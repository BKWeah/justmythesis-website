-- SUPPORT REQUESTS TABLE
-- ============================================================
-- Stores client service requests that may or may not become projects.
-- A support request is NOT a project until explicitly approved and converted.
-- Request reference format: JMT-YYYY-XXXXXX (generated by database function)

-- Create enum type for request status if not exists
