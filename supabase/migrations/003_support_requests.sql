-- Create the support_requests table
CREATE TABLE IF NOT EXISTS support_requests (
    -- Primary key
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    
    -- Reference number (unique, non-editable)
    request_reference TEXT UNIQUE NOT NULL DEFAULT generate_request_reference(),
    
    -- Client relationship
    client_id UUID NOT NULL REFERENCES clients(id) ON DELETE RESTRICT,
    
    -- Service information
    requested_service service_package,
    current_stage TEXT, -- e.g., 'Proposal', 'Literature Review', 'Data Collection', etc.
    
    -- Project details
    working_title TEXT,
    academic_level academic_level,
    submission_deadline DATE,
    
    -- Request details
    support_description TEXT,
    
    -- Status tracking
    status request_status NOT NULL DEFAULT 'New Request',
    priority priority_level NOT NULL DEFAULT 'Normal',
    
    -- Timestamps
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    
    -- Additional metadata
    source TEXT DEFAULT 'website', -- website, whatsapp, email, phone
    notes TEXT -- Internal notes
    
);

-- Create index on client_id for faster lookups
CREATE INDEX IF NOT EXISTS idx_support_requests_client_id 
    ON support_requests(client_id);

-- Create index on status for filtering
CREATE INDEX IF NOT EXISTS idx_support_requests_status 
    ON support_requests(status);

-- Create index on request_reference for quick lookups
CREATE INDEX IF NOT EXISTS idx_support_requests_reference 
    ON support_requests(request_reference);

-- Create index on created_at for sorting
CREATE INDEX IF NOT EXISTS idx_support_requests_created_at 
    ON support_requests(created_at DESC);

-- Create composite index for common query patterns
CREATE INDEX IF NOT EXISTS idx_support_requests_client_status 
    ON support_requests(client_id, status);

-- ============================================================
-- UPDATED_AT TRIGGER
-- ============================================================
-- Automatically update the updated_at timestamp when a record is modified

CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Create trigger for support_requests
DROP TRIGGER IF EXISTS trigger_support_requests_updated_at ON support_requests;
CREATE TRIGGER trigger_support_requests_updated_at
    BEFORE UPDATE ON support_requests
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

